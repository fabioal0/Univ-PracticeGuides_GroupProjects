package Aula_3;


public abstract class A3_EX2_Class_Figura {
    
    public double Perimetro(double Comp_Half_Circ, double Larg_Half_Circ){ return Comp_Half_Circ*2+Larg_Half_Circ*2; }
    
    public double Area(double Comp_sqr_Circ, double Larg_sqr_Circ){ return Comp_sqr_Circ*Larg_sqr_Circ; }
    
}
